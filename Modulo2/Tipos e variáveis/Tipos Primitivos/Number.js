const number = 12.432;

// Transforma numero para string
console.log(`Número transformado para string: ${number.toString}`);

// Retorna número com um n° de casas decimais
console.log(`\nNúmero com duas casas decimais: ${number.toFixed(2)}`);

// Transforma uma string em flaot
console.log(`\nString passada para float:`, parseFloat('13.04'));

// Transforma uma string em int
console.log(`\nString passada para int:`, parseInt('13,21'));


