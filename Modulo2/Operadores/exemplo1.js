// Modulo(%)
// Operador binário, retorna o inteiro restante da divisão dos dois operandos.

12 % 5 // Return 2

// Incremento (++) -> Soma +1 / (--) Soma -1, ou seja, diminui do valor existente
var x = 0;

++x;
x++;

--x;
x--;

// Negação (-) e Adição (+)
-3;
+'3';   // Return 3
+true;  // Return 1
+false; // Return 0
-true;  // Return -1

// Operador de exponenciação (**)
2 ** 3 // Return 8

// Operador de agrupamento ()
10 + (3 * -2)